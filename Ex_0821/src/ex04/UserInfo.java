package ex04;

public class UserInfo {
	private String id;
	private int pwd;
	
	//getter & setter
	public String getId() {
		return id;
	}
	
	public int getPwd() {
		return pwd;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	public void setPwd(int pwd) {
		this.pwd = pwd;
	}
	
}
